# list datastructures

my_list=["Kenya","Nigeria", "Tanzania", "Uganda", "India", "Qatar", "Dubai"]
my_num=[2,8,1,34,27,9,0,-1,23]
my_num.sort()
print(my_num)
my_list.sort()
print(my_list)
my_list[2]="South africa"
print(my_list[2])
print("I am from",my_list[0])
print(f"I am from {my_list[0]}")

# tuple data structure
my_tuple=("Bananas","Mangoes","Melons","Apples")
print(my_tuple)

# set data structure
my_set={"Toyota","Mercedes","BMW","Subaru"}
print(my_set)

# dictionaries data structures
my_dic={"name": "Kindo","age":18,"gender":"male"}
print(my_dic)
my_dic.pop("gender")
print(f"my age is {my_dic['age']}")
